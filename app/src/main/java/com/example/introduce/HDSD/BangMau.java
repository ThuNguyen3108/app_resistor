package com.example.introduce.HDSD;

public class BangMau {
    private int id;
    private  String vach1;
    private  String vach2;
    private  String vach3;
    private  String vach4;

    public BangMau(int id, String vach1, String vach2, String vach3, String vach4) {
        this.id = id;
        this.vach1 = vach1;
        this.vach2 = vach2;
        this.vach3 = vach3;
        this.vach4 = vach4;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getVach1() {
        return vach1;
    }

    public void setVach1(String vach1) {
        this.vach1 = vach1;
    }

    public String getVach2() {
        return vach2;
    }

    public void setVach2(String vach2) {
        this.vach2 = vach2;
    }

    public String getVach3() {
        return vach3;
    }

    public void setVach3(String vach3) {
        this.vach3 = vach3;
    }

    public String getVach4() {
        return vach4;
    }

    public void setVach4(String vach4) {
        this.vach4 = vach4;
    }

}
